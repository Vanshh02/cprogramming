#include<stdio.h>
int main(){
    int a=20;
    int b=5;
    int dif=a-b;
    printf("the difference of %d and %d is %d\n", a,b,dif);
    int sum=a+b;
    printf("the sum of %d and %d is %d\n", a,b,sum);
    int product=a*b;
    printf("the product of %d and %d is %d\n", a,b,product);
    int divide=a/b;
    printf("the divide of %d and %d is %d\n", a,b,divide);
    return 0;
}